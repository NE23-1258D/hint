// Expressフレームワークを読み込みます
const express = require('express');
// Node.jsのpathモジュールを読み込み、ファイルパスの操作に使用します
const path = require('path');

// Expressアプリケーションのインスタンスを作成します
const app = express();
// サーバーがリッスンするポート番号を定義します。
// 環境変数PORTが設定されていればそれを使用し、なければデフォルトで3000番ポートを使用します。
const PORT = process.env.PORT || 3000;

// ===============================================
// テンプレートエンジンと静的ファイルの配信設定
// ===============================================

// EJSをテンプレートエンジンとして設定します。
app.set('view engine', 'ejs');
// EJSテンプレートファイルが配置されているディレクトリを指定します。
// path.join(__dirname, 'views') は、現在のディレクトリ内の 'views' フォルダを指します。
app.set('views', path.join(__dirname, 'views'));

// 静的ファイルの配信設定を行います。
// 'public'ディレクトリ内のCSS、JavaScript、画像などのファイルがWebブラウザから直接アクセスできるようになります。
// 例: /css/main.css は public/css/main.css に対応します。
app.use(express.static(path.join(__dirname, 'public')));

// ===============================================
// ルーティング設定
// ===============================================

// ルートURL ('/') へのGETリクエストを処理します。
app.get('/', (req, res) => {
    // 'top.ejs' テンプレートをレンダリングしてクライアントに送信します。
    // 'views/top.ejs' ファイルが読み込まれ、HTMLとして出力されます。
    res.render('top');
});

// ===============================================
// サーバーの起動
// ===============================================

// 設定されたポートでサーバーを起動し、リクエストを待ち受けます。
app.listen(PORT, () => {
    console.log(`サーバーがポート ${PORT} で起動しました: http://localhost:${PORT}`);
    console.log('ブラウザで上記URLにアクセスしてください。');
});
